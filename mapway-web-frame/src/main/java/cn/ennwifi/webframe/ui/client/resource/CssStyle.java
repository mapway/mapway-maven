package cn.ennwifi.webframe.ui.client.resource;

import com.google.gwt.resources.client.CssResource;


/**
 * CSS 样式资源.
 *
 * @author zhangjianshe
 */
public interface CssStyle extends CssResource {

    /**
     * Tree item image.
     *
     * @return the string
     */
    String treeItemImage();

    /**
     * Tree item selected.
     *
     * @return the string
     */
    String treeItemSelected();

    /**
     * Css table.
     *
     * @return the string
     */
    String cssTable();

    /**
     * Row 0.
     *
     * @return the string
     */
    String row0();

    /**
     * Row 1.
     *
     * @return the string
     */
    String row1();

    /**
     * Rowh.
     *
     * @return the string
     */
    String rowh();


    /**
     * Row select.
     *
     * @return the string
     */
    String rowSelect();

    /**
     * Title.
     *
     * @return the string
     */
    String title();

    /**
     * Lb on.
     *
     * @return the string
     */
    String lbOn();

    /**
     * Lb off.
     *
     * @return the string
     */
    String lbOff();

    /**
     * Pagebtn on.
     *
     * @return the string
     */
    String pagebtnOn();

    /**
     * Pagebtn off.
     *
     * @return the string
     */
    String pagebtnOff();

    /**
     * Mqtt text.
     *
     * @return the string
     */
    String mqttText();

    /**
     * Lage content string.
     *
     * @return the string
     */
    String lageContent();
}
